class ASLLoss(nn.Module):
    def __init__(self, num_classes , gamma_positive , gamma_negative, clip=0, eps=0, disable_torch_grad_focal_loss=False, mask= None , device = 'cpu'):
        super(ASLLoss, self).__init__()
        self.num_classes = num_classes
        self.gamma_positive = gamma_positive
        self.gamma_negative = gamma_negative
        self.clip = clip
        self.eps = eps
        self.disable_torch_grad_focal_loss = disable_torch_grad_focal_loss
        self.mask = mask
        self.loss=ASLSingleLabel(gamma_pos=gamma_positive, gamma_neg=gamma_negative)
        
        
    def forward(self, x, y, mask, device):
        # Remove samples with mask = 0
        x = x[mask == 1]
        y = y[mask == 1] 
        
        if self.num_classes > 2:   
            loss = self.loss(x, y-1)  
        else:
            loss = self.loss(x, y)
        
        return loss
    